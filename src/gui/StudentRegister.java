    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String firstName = fName.getText();
        String lasttName = lName.getText();
        String nic = studentNic.getText();
        String mobile = studentMobile.getText();
        String line1 = line01.getText();
        String line2 = line02.getText();
        String city = studentCity.getText();

        if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Srudent First Name", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (lasttName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Srudent Last Name", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (nic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Srudent NIC", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (!nic.matches("^[0-9]{12}$")) {
            JOptionPane.showMessageDialog(this, "Invalid NIC Number", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Srudent Mobile", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[1245678]{1}[0-9]{7}")) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile Number", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address Line 01", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address Line 02", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else if (city.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter City", "Warnning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `student` WHERE `mobile` = '" + mobile + "'");

                if (!resultSet.next()) {
                    MySQL.executeIUD("UPDATE `student` SET `mobile` = '" + mobile + "', `line01` = '" + line1 + "', `line02` ='" + line2 + "', `city` = '" + city + "' WHERE `nic` = '" + nic + "'");
                    JOptionPane.showMessageDialog(this, "User Details Updated", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    loadStudent();                   
                } else {                    
                    JOptionPane.showMessageDialog(this, "Mobile Number Already Registerd", "Warnning", JOptionPane.WARNING_MESSAGE);

                }
                
                 reset();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton3ActionPerformed

