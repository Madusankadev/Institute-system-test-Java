    private Integer getStudentCount() {
        int studentCount = 0;
        try {
            ResultSet resultSet = MySQL.execute("SELECT COUNT(*) AS num FROM `student`");
            while (resultSet.next()) {
                studentCount = resultSet.getInt("num");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return studentCount;
    }

    private void loadStudent() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        jTable1.setRowHeight(32);

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `student`");

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();

                StudentData studentData = new StudentData();

                studentData.setId(Integer.valueOf(resultSet.getString("id")));
                studentData.setNic(String.valueOf(resultSet.getString("nic")));
                studentData.setFirstName(String.valueOf(resultSet.getString("firstName")));
                studentData.setLastName(String.valueOf(resultSet.getString("lastName")));
                studentData.setMobile(String.valueOf(resultSet.getString("mobile")));
                studentData.setLine01(String.valueOf(resultSet.getString("line01")));
                studentData.setLine02(String.valueOf(resultSet.getString("line02")));
                studentData.setCity(String.valueOf(resultSet.getString("city")));
                studentData.setsCardId(String.valueOf(resultSet.getString("sCardId")));

                studentMap.put(String.valueOf(resultSet.getString("sCardId")), studentData);

                String name = String.valueOf(resultSet.getString("firstName") + " " + resultSet.getString("lastName"));

                vector.add(String.valueOf(resultSet.getString("sCardId")));
                vector.add(name);

                model.addRow(vector);
            }

            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

