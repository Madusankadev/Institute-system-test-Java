    public void getRealTimeDate() {
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    Date date = new Date();
                    SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");
                    String finalDate = format.format(date);
                    jLabel2.setText(finalDate);

                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
            }
        });

        t.start();
    }

    private void loadStudent() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        jTable1.setRowHeight(32);

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `student`");

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();

                String name = String.valueOf(resultSet.getString("firstName")) + " " + String.valueOf(resultSet.getString("lastName"));

                vector.add(String.valueOf(resultSet.getString("sCardId")));
                vector.add(name);
                vector.add(String.valueOf(resultSet.getString("mobile")));

                model.addRow(vector);
            }

            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

