    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int Row = jTable1.getSelectedRow();

        if (evt.getClickCount() == 2) {
            MarkAttendance ma = new MarkAttendance(this, true, String.valueOf(jTable1.getValueAt(Row, 0)));
            ma.setVisible(true);
        }

        //set past report
        jLabel6.setText(String.valueOf(jTable1.getValueAt(Row, 1)));
        jLabel8.setText(String.valueOf(jTable1.getValueAt(Row, 0)));

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `attendance` INNER JOIN `student` ON `attendance`.`student_id` = `student`.`id` WHERE `student`.`sCardId` = '" + String.valueOf(jTable1.getValueAt(Row, 0)) + "'");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();

                vector.add(String.valueOf(resultSet.getString("date")));
                vector.add(String.valueOf(resultSet.getString("state")));

                model.addRow(vector);

            }
            jTable2.setRowHeight(32);
            jTable2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jTable1MouseClicked

